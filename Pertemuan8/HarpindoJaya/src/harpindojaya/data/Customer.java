package harpindojaya.data;

/**
 *
 * @author Lenovo
 */
public class Customer {
    private String id;
    private String name;
    private String tipe;
    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getTipe() {
        return tipe;
    }

    public void setTipe(String tipe) {
        this.tipe = tipe;
    }
}
